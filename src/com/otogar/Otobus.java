package com.otogar;

public class Otobus {
	
	int otobusId;
	String plaka;
	Insan otobusInsan;
	
	public int getOtobusId() {
		return otobusId;
	}
	public void setOtobusId(int otobusId) {
		this.otobusId = otobusId;
	}
	public String getPlaka() {
		return plaka;
	}
	public void setPlaka(String plaka) {
		this.plaka = plaka;
	}
	public Insan getOtobusInsan() {
		return otobusInsan;
	}
	public void setOtobusInsan(Insan otobusInsan) {
		this.otobusInsan = otobusInsan;
	}
	
	

}
