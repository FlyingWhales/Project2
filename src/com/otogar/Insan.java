package com.otogar;

public class Insan {
	int insanId;
	String insanName;
	Meslek insanMeslek;

	public int getInsanId() {
		return insanId;
	}

	public void setInsanId(int insanId) {
		this.insanId = insanId;
	}

	public String getInsanName() {
		return insanName;
	}

	public void setInsanName(String insanName) {
		this.insanName = insanName;
	}

	public Meslek getInsanMeslek() {
		return insanMeslek;
	}

	public void setInsanMeslek(Meslek insanMeslek) {
		this.insanMeslek = insanMeslek;
	}

}
